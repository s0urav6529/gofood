const mongoose = require("mongoose");

const foodItemSchema = mongoose.Schema(
  {
    categoryName: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    img: {
      type: String,
      required: true,
    },
    option: {
      type: Object,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("foodItem", foodItemSchema);
